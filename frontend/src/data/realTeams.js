// HELPER FUNCTIONS FOR CONSISTENT DATA ACCESS
export const getTeamById = (id) => {
  return REAL_TEAMS.find(team => team.id === parseInt(id));
};

export const getTeamsByRegion = (region) => {
  if (region === 'all') return REAL_TEAMS;
  return REAL_TEAMS.filter(team => team.region === region);
};

export const getRandomTeams = (count = 2) => {
  const shuffled = [...REAL_TEAMS].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomTournament = () => {
  return REAL_TOURNAMENTS[Math.floor(Math.random() * REAL_TOURNAMENTS.length)];
};

