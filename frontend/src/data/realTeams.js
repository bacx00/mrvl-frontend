// REAL BACKEND PLAYER DATA CACHE - for quick lookup
export const REAL_PLAYERS_CACHE = {};

// Function to fetch and cache real players from backend
export const fetchRealPlayers = async (api) => {
  try {
    console.log('🔄 Fetching real players from backend API...');
    const response = await api.get('/players');
    const players = response?.data?.data || response?.data || [];
    
    if (Array.isArray(players) && players.length > 0) {
      // Cache players by team_id for quick team roster lookup
      players.forEach(player => {
        if (player.team_id) {
          if (!REAL_PLAYERS_CACHE[player.team_id]) {
            REAL_PLAYERS_CACHE[player.team_id] = [];
          }
          REAL_PLAYERS_CACHE[player.team_id].push({
            id: player.id,
            name: player.name || player.gamer_tag || `Player${player.id}`,
            role: player.role || 'Duelist',
            main_hero: player.main_hero || 'Iron Man',
            team_id: player.team_id,
            country: player.country || 'US'
          });
        }
      });
      console.log('✅ Real players cached:', Object.keys(REAL_PLAYERS_CACHE).length, 'teams');
      return players;
    }
  } catch (error) {
    console.error('❌ Failed to fetch real players:', error);
  }
  return [];
};

// Get real players for a team
export const getRealPlayersForTeam = (teamId) => {
  return REAL_PLAYERS_CACHE[teamId] || [];
};
